"""My Calculator Functions"""

def add(a,b):
    """This function does addition"""
    return a + b

def subtract(a,b):
    """This function does subtraction"""
    return a - b

def multiply(a,b):
    """This function does multiplication"""
    return a * b

def divide(a,b):
    """This function does division"""
    return a / b

def remainder(dividend,divisor):
    """This function does modulus"""
    return dividend % divisor
